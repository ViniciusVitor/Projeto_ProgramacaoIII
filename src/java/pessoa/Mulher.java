package pessoa;

/**
 *
 * @author vinicius
 */
public class Mulher {
    private String nome;
    private String idade;
    private String ciclo;
    private String data;

    public Mulher() {
        this.nome = nome;
        this.idade = idade;
        this.ciclo = ciclo;
        this.data = data;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getIdade() {
        return idade;
    }

    public void setIdade(String idade) {
        this.idade = idade;
    }

    public String getCiclo() {
        return ciclo;
    }

    public void setCiclo(String ciclo) {
        this.ciclo = ciclo;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    
    
    
        
    
    
}
